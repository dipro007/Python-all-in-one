mark = float(input("Enter mark = "))

if mark >= 80:
    print("Grade is A+")
elif mark >= 70:
    print("Grade is A")
elif mark >= 60:
    print("Grade is A-")
elif mark >= 50:
    print("Grade is B")
elif mark >= 40:
    print("Grade is C")
elif mark >= 33:
    print("Grade is D")

else:
    print("Failed try again next time")



